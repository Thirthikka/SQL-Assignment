###GROUP BY + HAVING
#List languages that have more than 100 movies.
SELECT lang,COUNT(*)
FROM movies
GROUP BY lang
HAVING COUNT(*) > 100;

#List actors who acted in more than 10 movies.
SELECT act_name,COUNT(*)
FROM movies
GROUP BY act_name
HAVING COUNT(*) > 10;
#List directors who directed at least 5 movies.
SELECT dir_name,COUNT(*)
FROM movies
GROUP BY dir_name
HAVING COUNT(*) >= 5;
#Find years where more than 50 movies were released.
SELECT rel_year,COUNT(*)
FROM movies
GROUP BY rel_year
HAVING COUNT(*) > 50;
#List actors who have worked in more than 3 languages.
SELECT act_name,COUNT(DISTINCT lang)
FROM movies
GROUP BY act_name
HAVING COUNT(DISTINCT lang) > 3;

















